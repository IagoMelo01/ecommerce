        <p>O prazo para devolução por desistência / arrependimento é de até 07 dias a partir da data de recebimento da mercadoria.</p>

        <p>POLÍTICA DE DEVOLUÇÃO TROCAS POR DEFEITO OU PRODUTO AVARIADO:</p>

        <p>Garantimos trocas por defeitos de fabricação e por produtos que chegarem avariados, para esses casos não há custo de frete para o comprador.</p>

        <P>PRODUTO COM DEFEITO</P>

        <P>Se o produto recebido estiver com defeito você poderá solicitar a troca no período de até 7 dias corridos, a contar da data de entrega, através do e-mail atendimento@divaspink.com.br.</P>

        <p>Para produtos com defeitos de fabricação avaliados pelo fabricante, o prazo para troca é de 30 dias após o recebimento da mercadoria.</p>

        <p>DEFEITO</p>

        <p>Para devolver um produto por defeito, as seguintes condições deverão ser observadas:</p>

        <p>Todos os produtos passarão por uma prévia análise técnica para verificação da existência de defeito de fabricação. Caso seja constatado que não há defeito no produto, ou descumprimento de alguma das condições descritas no tópico anterior, o(a) Divas Pink providenciará o envio do mesmo produto ao endereço do Cliente, sendo o frete pago pelo cliente.</p>

        <p>O(A) Divas Pink isenta-se da obrigação de trocar ou consertar qualquer produto cujo defeito tenha sido ocasionado por mau uso, de acordo com o laudo que nos é fornecido pela Assistência Técnica do fabricante.</p>

        <p>Verifique os seguintes aspectos no momento do recebimento: (i) se a embalagem estiver aberta, violada ou danificada; (ii) se o produto estiver avariado pelo transporte, aberto, violado ou usado; (iii) se os produtos entregues não corresponderem aos produtos adquiridos pelo site e/ou a nota fiscal; ou, ainda, (iv) se o conteúdo das embalagens estiver incompleto, sem partes ou acessórios dos produtos adquiridos. Em caso de qualquer divergência, recuse-se a receber o produto em questão.</p>

        <p>Caso haja o recebimento indevido do produto, poderá requerer sua troca apenas nos casos previstos na Política de Trocas e Devoluções.</p>
